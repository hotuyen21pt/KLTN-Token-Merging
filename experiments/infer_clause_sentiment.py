# -*- coding: utf-8 -*-
"""
Split one sentence into clauses+terms by Phi-3, then run APC sentiment inference per clause.

Run from repo root (parent of ``thesis_apc_baseline``), e.g. ``kltn``:

  python thesis_apc_baseline/experiments/infer_clause_sentiment.py ^
    --checkpoint thesis_apc_baseline/checkpoints/fast_lcf_bert_tome/<run_folder> ^
    --sentence "The pizza was great but the staff was rude and the restaurant was noisy."
"""

from __future__ import annotations

import argparse
import json
import os
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parents[2]
if str(ROOT) not in sys.path:
    sys.path.insert(0, str(ROOT))

from thesis_apc_baseline.models.llm_model import (
    clauses_to_infer_one_inputs,
    infer_sentiment_from_sentence_with_phi3_split,
    split_sentence_with_terms_llm,
)
from thesis_apc_baseline.experiments.apc_inference import to_json_serializable


def _extract_sentiment_fields(pred: object) -> tuple[str, str]:
    """Best-effort extraction for label/confidence from various PyABSA outputs."""
    sentiment = ""
    confidence = ""
    if isinstance(pred, dict):
        sentiment = str(
            pred.get("sentiment")
            or pred.get("label")
            or pred.get("polarity")
            or pred.get("sentiment_label")
            or ""
        )
        conf = pred.get("confidence")
        if conf is None:
            conf = pred.get("score")
        if isinstance(conf, (int, float)):
            confidence = f"{float(conf):.4f}"
        elif conf is not None:
            confidence = str(conf)
    return sentiment, confidence


def _print_pretty_table(outputs: list[dict[str, object]]) -> None:
    rows: list[tuple[str, str, str, str, str]] = []
    for item in outputs:
        inp = item.get("input", {})
        pred = item.get("prediction", {})
        if isinstance(pred, list) and pred:
            pred = pred[0]
        clause = ""
        aspect = ""
        opinion = ""
        if isinstance(inp, dict):
            clause = str(inp.get("sentence", ""))
            aspect = str(inp.get("aspect", ""))
            opinion = str(inp.get("opinion", ""))
        sentiment, confidence = _extract_sentiment_fields(pred)
        rows.append((clause, aspect, opinion, sentiment, confidence))

    headers = ("clause", "aspect", "opinion", "sentiment", "confidence")
    widths = [len(h) for h in headers]
    for row in rows:
        for i, col in enumerate(row):
            widths[i] = min(max(widths[i], len(col)), 80)

    def _fmt(val: str, w: int) -> str:
        return (val[: w - 3] + "...") if len(val) > w else val.ljust(w)

    print("=== SENTIMENT TABLE ===")
    print(" | ".join(_fmt(h, widths[i]) for i, h in enumerate(headers)))
    print("-+-".join("-" * w for w in widths))
    for row in rows:
        print(" | ".join(_fmt(row[i], widths[i]) for i in range(5)))


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Clause-level APC inference: Phi-3 split + PyABSA sentiment per clause."
    )
    parser.add_argument(
        "--checkpoint",
        type=str,
        required=True,
        help="Folder containing PyABSA model files (.state_dict/.config/.tokenizer).",
    )
    parser.add_argument(
        "--sentence",
        type=str,
        required=True,
        help="Input review sentence.",
    )
    parser.add_argument(
        "--phi3-model",
        type=str,
        default="microsoft/Phi-3-mini-4k-instruct:featherless-ai",
        help="HF Router model id for clause splitting.",
    )
    parser.add_argument(
        "--max-new-tokens",
        type=int,
        default=300,
        help="Max tokens generated by Phi-3 for splitting output.",
    )
    parser.add_argument(
        "--show-intermediate",
        action="store_true",
        help="Also print split clauses and infer-one formatted inputs.",
    )
    parser.add_argument(
        "--pretty",
        action="store_true",
        help="Print compact table: clause | aspect | sentiment | confidence.",
    )
    parser.add_argument(
        "--hf-token",
        type=str,
        default=None,
        help="Optional Hugging Face token. If provided, it sets HF_TOKEN for this run.",
    )
    args = parser.parse_args()

    if args.hf_token:
        os.environ["HF_TOKEN"] = args.hf_token
    elif not os.environ.get("HF_TOKEN"):
        parser.error(
            "HF token is required for Phi-3 split. Set environment variable HF_TOKEN "
            "or pass --hf-token <token>."
        )

    if args.show_intermediate:
        clauses = split_sentence_with_terms_llm(
            sentence=args.sentence,
            model=args.phi3_model,
            tokenizer=None,
            max_new_tokens=args.max_new_tokens,
        )
        infer_inputs = clauses_to_infer_one_inputs(clauses)
        print("=== CLAUSES ===")
        print(json.dumps(clauses, ensure_ascii=False, indent=2))
        print("=== INFER INPUTS ===")
        print(json.dumps(infer_inputs, ensure_ascii=False, indent=2))

    outputs = infer_sentiment_from_sentence_with_phi3_split(
        sentence=args.sentence,
        checkpoint_dir=args.checkpoint,
        model=args.phi3_model,
        tokenizer=None,
        max_new_tokens=args.max_new_tokens,
    )
    if args.pretty:
        _print_pretty_table(outputs)

    print("=== SENTIMENT OUTPUT ===")
    print(json.dumps(to_json_serializable(outputs), ensure_ascii=False, indent=2))


if __name__ == "__main__":
    main()

